//Faça um programa que leia um valor do tipo float e depois o imprima usando o operador “%d”. Veja o que aconteceu.

#include <stdio.h>
#include <stdlib.h>

int main(){
    float numero;
    printf("digite um numero: ");
    scanf("%d",&numero);
    printf("%d",numero);
    return 0;
}

//Resposta resultado foi 0